const normalizeLocale = (value) => {
  const locale = String(value || "").trim();
  if (!locale) return DEFAULT_LOCALE;
  if (locale === "zh-Hant" || locale.toLowerCase().startsWith("zh")) return "zh-Hant";
  if (locale.toLowerCase().startsWith("en")) return "en";
  return DEFAULT_LOCALE;
};

const readStoredLocale = () => {
  try {
    return window.localStorage.getItem("frontline_studio_locale");
  } catch {
    return "";
  }
};

export const resolveInitialLocale = () => {
  if (typeof window === "undefined") return DEFAULT_LOCALE;
  const params = new URLSearchParams(window.location.search || "");
  return normalizeLocale(params.get("lang") || readStoredLocale() || DEFAULT_LOCALE);
};

export const localeHtmlLang = (locale) => (
  SUPPORTED_LOCALES.find((option) => option.code === normalizeLocale(locale))?.htmlLang || "en"
);

export const translate = (locale, key, vars = {}) => {
  const dictionary = dictionaries[normalizeLocale(locale)] || dictionaries[DEFAULT_LOCALE];
  const template = dictionary[key] ?? dictionaries[DEFAULT_LOCALE][key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => {
    const value = vars[name];
    return value === undefined || value === null ? "" : String(value);
  });
};
